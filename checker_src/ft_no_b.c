#include "checker.h"

void	ft_no_b(t_inf *a_b)
{
	if (a_b->b_value_quantity == 0)
	{
		ft_putstr("KO\n");
		ft_free(a_b->stack_a);
		a_b->stack_a = NULL;
		ft_free(a_b->stack_b);
		exit(1);
	}
}
